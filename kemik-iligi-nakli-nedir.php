								Kemik iliği ya da kök hücre nakli iki şekilde yapılabilir. Sağlıklı kök hücreler bir kişiden diğer kişiye (allojeneik) nakledilebilir ya da kişinin kendi (otolog) hücreleri önceden toplanıp dondurularak daha sonra kendisine aktarılabilir. Allojeneik kök hücre kaynağı olarak HLA uyuşumlu sağlıklı akraba ya da akraba dışı vericiler, kordon kanı ya da yarı yarıya uyuşumlu anne-baba-çocuk ya da kardeşler kullanılabilir. Otolog kök hücre kaynağı, kişinin kendisidir. <br>
