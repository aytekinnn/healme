								Kemik iliği nakli nasıl yapılır? <br><br>

								Kök hücreler hastaya damar yolu aracılığı ile verilir. Bunun için genellikle santral ve nözkateter adı verilen, damar içi yerleştirilen aparatlar kullanılır. <br>

								Kemik iliği naklinin aşamaları nelerdir? <br><br>

								Nakil ünitesi içinde geçen süreci üç aşamaya ayırabiliriz. İlk aşama "hazırlık rejimi" adını verdiğimiz tedavi sürecidir. Bu dönemde hastalığa ve hastanın yaşına göre uygun olan bir tedavi seçilir. Tedavi süresi uygulanan protokole göre değişir. Hazırlık rejimin özetle üç temel amacı vardır. Öncelikle hastada tespit edilemeyecek orandaki hastalıklı hücrelerin yok edilmesi, hastanın kemik iliğinin boşaltılarak verici hücrelerine yer açılması ve hastanın bağışıklık sistemi baskılanarak verici hücrelerine reaksiyon göstermesinin engellenmesi amaçlanır. İkinci aşama kök hücrelerin verilmesidir. Üçüncü aşama ise vericinin kök hücrelerinin, alıcının kemik iliğinde çoğalması aşamasıdır. <br><br>
								Hastalar nakil ünitesinden taburcu olduktan sonra nakil polikliniğinde takip edilirler. Takip sıklıkları durumlarına göre değişmekle birlikte ilk 3 aylık erken dönemde haftada bir ya da iki haftada bir olacak şekildedir. İlk bir yıl dolana kadar, hastanın düzenli olarak her ay kontrollerinin gerçekleştirilmesi gerekir. İleri dönemde hastanın durumuna göre kontrol süreleri belirlenir. <br>

								Kemik iliği nakli sonrası görülebilecek sorunlar nelerdir? <br>

								Nakil sonrası erken ve geç dönemde karşılaşılan sorunların başında graft versus host hastalığı (GVHH)gelir. Bu vericiye ait bağışıklık sistemi hücrelerinin alıcıya karşı başlattığı bir savaştır ve vücudun çeşitli dokularında hasar oluşturur. En sık hasar gören dokular cilt, karaciğer ve mide-bağırsak sistemidir. Ciltte kızarıklık, kaşıntı, mide-bağırsak sisteminde bulantı, kusma, ishal ve karaciğerde testlerde bozulma ile ortaya çıkar. Bu hastalık, HLA tam uyuşumlu kardeş vericilerden yapılan nakillerde %30-40, akraba dışı vericilerden yapılan nakillerde ise %60-70 oranında görülür. <br>
								Verilen kök hücrelerin alıcı kemik iliğinde yerleşip çoğalamaması ikinci önemli sorundur. Kan değerlerine bakılarak değerlendirilir ve bu koşulda yeniden nakil yapılması gündeme gelir. Hastalığın nakilden sonra yeniden ortaya çıkması diğer bir sorundur. Nakil sonrası hasta enfeksiyonlara açık durumdadır ve kendilerini enfeksiyondan korunmaları çok önemlidir. <br>

