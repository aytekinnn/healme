                        <p>Gözlerin gördüğü görüntünün ne kadar net olduğunu anlamaya yardımcıdır. 6 metre uzaklıktaki gittikçe küçülen harflerin okunması ile test yapılır.</p>
                        <h4>Görme Alanı Testi</h4>
                        <p>Bakışları gözün karşısındaki tek bir noktaya sabitledikten sonra noktanın etrafındaki bütün görebilen alan görme alanıdır. Göz tansiyonu tanısında ve takibinde sıklıkla uygulanan bir testtir.</p>
                        <h4>Tonometre</h4>
                        <p>Göz içi sıvı basıncın ölçülmesinde kullanılır. Göze noncontact tonometre cihazından hava püskürtülerek ölçüm yapılır. Eğer göz basıncı normal değerin üzerindeyse, pakometre cihazıyla ses dalgaları kullanılarak kornea kalınlığı ölçülür. Göz tansiyonu tanı ve takibinde kullanılmaktadır.</p>
                        <h4>Retinoskop (Skiyaskop) ve Otorefraktometre</h4>
                        <p>Miyop ve hipermetrop gibi gözde kırma kusurlarına neden olan hastalıklarda mercek numarasının derecelendirilmesinde kullanılır. <br><br>
                            Görme, kuşkusuz en önemli duyumuzdur. Çoğu insan için görme duyusunun kaybedilmesinin korkusu, yaşamın kaybedilmesi ile eşdeğerdir. Bu derece hassas olduğumuz gözlerimizin hastalıkları için de düzenli doktor kontrolüne gidilmeli, belirlenen randevu tarihleri aşılmamalıdır. <br><br>
                            Hiçbir görme problemi ya da gözle ilişkili şikayeti olmayan bireylerin 55 yaşından önce 2 yılda 1, 55 yaşından sonra ise yılda 1 kez göz muayenesine gitmesi önerilir. <br><br>
                            Diyabet, Graves, hipertansiyon, sarkoidoz ve Behçet gibi sistemik hastalığa sahip ya da ailede görme kaybı benzeri öyküsü bulunan bireyler göz hastalıkları açısından normal topluma göre riskli grupta yer alır. Tanı alınan yaştan itibaren daha sık göz muayenelerine gitmeleri önerilir.
