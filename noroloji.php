                            <h5>Nörolojik Tanıda Kullanılan Testler ve Görüntüleme Yöntemleri</h5>
                        </center>

                        <p>Nöroloji bölümünde hastaya mental, duyu, kranial sinir ve refleks muayeneleri yapılmaktadır. Tanıda da hastanın anlattığı hikaye ve yapılan muayenenin sonucuna göre gerekli olduğu düşünülen görüntüleme yöntemleri kullanılır. <br>
                            <b> Bilgisayarlı tomografi (BT)</b> <br>
                            <b> Manyetik rezonans görüntüleme (MRG)</b> <br>
                            <b> Elektroensefalografi (EEG) :</b> EEG testi, beyinde oluşturulan elektriksel dalgalanmaların elektronik cihazlar vasıtası ile görüntülenmesi işlemidir. Elektrotlar özel yapıştırıcılarla kafa derisine yerleştirilmektedir. Epilepsi tanı takip ve tedavisinde kullanılır. EEG, uyku ve uyanıklık sırasında çekilebilir. <br>
                            <b> Elektromiyografi (EMG) :</b> Tuzak nöropatiler (karpal tünel sendromu gibi),kas hastalıkları ve Myastenia Gravis gibi nöromuskuler hastalıkların tanısında kullanılır. <br>
                            <b> Polisomnografi (PSG) :</b> Ev ya da hastane ortamında uygulanabilir. Ev ortamında alınan sonuç daha sağlıklıdır. Uyku kesintilerini ve uykudaki solunum bozukluklarını (apne vb.) göstermek amacıyla yapılmaktadır.
                        </p>

                        <h5>Nörolojik Tedavi Yöntemleri</h5>
                        <p>Nörolojik hastalıkların tedavisi, hastalığın kötü ilerleyişini durdurmak, semptomların sıklığını azaltmak mümkünse de hastalığı ortadan kaldırmak amacıyla yapılmaktadır. İlaç tedavileri ile eksik olan nörotransmitter madde yerine konulmaya çalışılır, eşlik eden psikolojik rahatsızlık varsa ona yönelik ek tedavi başlanır. İlaç tedavileri sıklıkla ilaçların kombinasyonu şeklindedir. <br><br>
                            Beyinde çeşitli nedenlerle oluşan kanama ve damar tıkanması gibi yaşamı tehdit eden ciddi hastalıklar, yoğun bakım ortamında nöroloji, yoğun bakım uzmanı ve beyin cerrahi uzmanları ile ortaklaşa takip ve tedavi edilir. <br><br>
                            <b> Nörolojik rehabilitasyon tedavisi: </b> MS, ALS ve felç gibi nörolojik hastalıklara sahip hastalara, fiziksel ve psikolojik olarak değerlendirilmeler yapılır. Hastada meydana gelen fiziksel ve duygusal hasarı en aza indirgemeye çalışır. Fiziksel rehabilitasyon ile hastanın kaybedilmiş ya da kısıtlanmış olan hareket işlevlerinin yeniden kazandırılması, hastanın günlük yaşam aktivitelerini bağımsız bir birey olarak devam ettirmesi amaçlanır. <br><br>
                            Nörolojik hastalıklarda cerrahi işlem gerektiren uygulamaları “Nöroşirürji” branşı yapmaktadır.
                        </p>


                        
                        

