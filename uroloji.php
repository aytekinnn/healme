                                • Geçirilmiş enfeksiyonlar <br>
                                • Testislerde görülen travmatik durumlar <br>
                                • Orak hücreli anemi gibi kronik hastalıklar <br>
                                • Kanser tedavisi yer alır. <br> <br>
                                <b> Erkekte kısırlığın tanı ve tedavisi</b> <br>
                                Bir sene boyunca düzenli cinsel ilişkiye rağmen çocuk sahibi olamayan erkeklere hekim, genel bir fizik muayene yapar ve tıbbi öyküyü almaya yönelik sorular yöneltir. Fizik muayene esnasında doğurganlığı etkileyebilecek kalıtsal hastalıklar, kronik sağlık sorunları, yaralanmalar, geçirilen enfeksiyonlar sorgulanır. Bunun yanında semen analizi de gereklidir. Bu analiz sayesinde menide yeteri kadar spermin varlığı, boyutları ve spermlerin hareket yeteneği değerlendirilmektedir. <br>
