                            <h5>Erkek İnfertilitesi (Erkekte Kısırlık) Nasıl Tedavi Edilir? </h5>
                            <p>Dünya üzerinde yaklaşık olarak her 7 çiftten biri kısırdır. İnfertil (kısırlık) durum bir sene boyunca sık ve korunmasız cinsel ilişkiye rağmen çocuk sahibi olamamayı ifade eder. Çocuk sahibi olamayan çiftlerin neredeyse yarısı erkek infertilitesi sebebiyle tedavi altına alınmaktadır. <br><br>
                                <b> Erkekte kısırlığın nedenleri</b> <br>
                                Erkekte kısırlık probleminin görülmesi ve bunun tedavi süreci kadınlara göre daha karmaşık bir yolculuğu kapsar. Bir erkeğin çocuk sahibi olabilmesi için; <br>
                                • Sağlıklı bir şekilde sperm üretebilmesi <br>
                                • Üretilen spermin meni adı verilen sıvıya taşınması <br>
                                • Menide yeterli sayıda spermin olması <br>
                                • Spermin fonksiyonel ve hareketli olması gerekir. <br>
                                Yukarıda bahsedilen kısırlık nedenleri arasında erkekte varikosel, enfeksiyon, ereksiyon sorunları, sperm hücrelerine saldıran antikorlar, tümörler, inmemiş testis varlığı, hormon dengesizlikleri, spermi taşıyan tübüllerde meydana gelen anormallikler, kromozom hastalıkları, erektil disfonksiyon, çölyak hastalığı, vazektomi gibi önceden geçirilen ameliyatlar ve bazı ilaçlar sayılabilir. Çevresel nedenler olarak endüstriyel kimyasallar, kurşun gibi ağır metallere maruz kalma, radyasyon ya da X ışınları, testislerin aşırı derecede ısınması örnek verilebilir. <br><br>
