                            Мы хотели бы упомянуть еще одно приложение, которое важно и будет полезно для наших пациентов. «Кросс-трансплантаты» в Турции. Кросс-трансплантация - это тип трансплантации, при котором подходящие доноры пациентов, ожидающих трансплантации органов, отдают органы реципиентам друг друга. <br><br>
                            Кросс-трансплантаты могут выполняться между пациентами, зарегистрированными в любом центре трансплантации органов, по обоюдному согласию, без обращения в комитет по этике. <br><br>

