                            Органы и ткани для трансплантации могут быть взяты из живых тел или трупов. <br><br>

                            Органы и ткани, которые сегодня можно успешно трансплантировать, включают сердце, почки, печень, легкие, поджелудочную железу, кишечник, матку, роговицу и костный мозг. <br><br>
                            Здесь мы хотели бы сделать важное напоминание нашим иностранным гостям, которые планируют приехать в Турцию для трансплантации органов и тканей. <br><br>
                            Согласно закону, операции по трансплантации, проведенные путем взятия органа из трупа в Турции, могут применяться только к гражданам Турции. <br><br>
                            По этой причине наши пациенты, которые приедут в Турцию на трансплантацию, обязательно должны приехать со своими живыми донорами. Доноров из Турции найти невозможно. <br><br>

                            Живой донор должен быть родственником пациента как минимум 4-й степени. Родственные отношения 4-й степени можно описать следующим образом. <br><br>
                            Родственники 1 степени: мать, отец, дети <br><br>
                            Родственники 2-й степени: бабушки и дедушки, братья и сестры, внуки. Родственники 3-й степени: дядя, тетя, племянницы, племянники. <br><br>
                            Родственники 4-й степени: двоюродные братья и сестры. <br><br>
                            Если реципиент состоит в браке, донорами могут быть также родственники супруга пациента до 4 степени. <br><br>
                            Принимаются напрямую пациенты, которые могут доказать свое родство до 4-й степени выше официальными записями, которые они привезут из своей страны. <br><br>
                            Пациенты, которые не могут найти донора от родственников до 4-й степени, попадают в комитет, называемый этическим комитетом, если они находят кандидата в доноры от дальних родственников или знакомых. Если комитет по этике понимает, что между пациентом и донором нет финансовых отношений, перевод разрешен. <br><br>

                            Когда нам отправляют медицинскую информацию о пациенте и доноре, проводится быстрая оценка, и их приглашают в Турцию, если они сочтут целесообразным для трансплантации. В нашей больнице проходят необходимые обследования и завершается подготовка к переводу. <br><br>
                            Продолжительность пребывания в Турции для трансплантации можно приблизительно указать следующим образом. <br><br>
                            -Пересадка почки: 1-1,5 месяца <br><br>
                            -Пересадка печени: 1,5-2месяцев <br><br>
                            -Пересадка роговицы: 2-3 недели <br><br>
                            -Пересадка костного мозга: 12 месяцев <br><br>

