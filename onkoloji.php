						<h3>Kanser Nasıl Teşhis Edilir?</h3>
						<div class="mt-0 mt-lg-4"></div>
						<p>Kanseri en erken evrelerinde teşhis etmek, genellikle tedavi için en iyi şansı sağlar. </p>
						<div class="row">
							<div class="col-md-6">
								<ul>
									<li> <b> Fizik Muayene: </b> Doktorunuz, kansere işaret edebilecek yumrular için vücudunuzun bölgelerini muayene esnasında hissedebilir. Fizik muayene sırasında ayrıca cilt rengindeki değişiklikler veya bir organın büyümesi gibi kanser varlığını gösterebilecek anormallikler de görülebilir.</li>
									<li> <b> Laboratuvar Testleri: </b> İdrar ve kan testleri gibi laboratuvar tetkikleri, hekime kansere yol açan nedenleri anlaması konusunda yol gösterici olabilir. Örneğin, lösemi hastalarında, tam kan sayımı adı verilen yaygın bir kan testi, olağandışı sayıda veya türde beyaz kan hücresi ortaya çıkarabilir. </li>
								</ul>
							</div>
							<div class="col-md-6">
								<ul>
									<li> <b> Görüntüleme Testleri: </b> Görüntüleme testleri, doktorunuzun kemiklerinizi ve iç organlarınızı noninvaziv olarak incelemesini sağlar. Kanser tanısında kullanılan görüntüleme yöntemleri; bilgisayarlı tomografi (CT) taraması, kemik taraması, manyetik rezonans görüntüleme (MRI), pozitron emisyon tomografisi (PET) taraması, ultrason ve X-ışınıdır.</li>
									<li> <b> Biyopsi: </b> Biyopsi sırasında doktorunuz laboratuvarda test için bir hücre örneği toplar. Örnek toplamanın birkaç yolu vardır. Hangi biyopsi prosedürünün sizin için uygun olduğu kanser türünüze ve bulunduğu yere bağlıdır. Çoğu durumda, kanseri kesin olarak teşhis etmenin tek yolu biyopsidir. </li>
								</ul>
							</div>
