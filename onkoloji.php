						<h3>Kanser Tedavisi Nasıldır?</h3>
						<p>Kanser tedavisi, kanseri tedavi etmek, küçültmek veya kanserin ilerlemesini durdurmak için cerrahi, radyasyon, ilaçlar ve diğer tedavilerin kullanılmasıdır. Birçok kanser tedavisi mevcuttur. Özel durumunuza bağlı olarak, Türkiye’de tedavi planları uygulanır. Kanser tedavisinin amacı, kanserinize bir çare bulmak ve normal bir yaşam sürmenizi sağlamaktır. Bir tedavi mümkün değilse, tedavileriniz kanserinizi küçültmek veya mümkün olduğunca uzun süre semptomsuz yaşamanızı sağlamak, kanserin büyümesini yavaşlatmak için kullanılabilir. <br><br>
							Kanser tedavileri şu şekildedir:

						</p>
						<div class="mt-3 mt-md-5 px-1 pt-1 pb-15 pt-md-2 px-md-4 bg-grey mb-8">
							<div id="faqAccordion1" class="faq-accordion" data-children=".faq-item">
								<div class="faq-item">
									<a data-toggle="collapse" data-parent="#faqAccordion1" href="#faqItem7" aria-expanded="true"><span>1.</span>Birincil Tedavi</a>
									<div id="faqItem7" class="collapse show faq-item-content" role="tabpanel">
										<div>
											<p>Birincil tedavinin amacı, kanseri vücuttan tamamen çıkarmak veya tüm kanser hücrelerini öldürmektir. Herhangi bir kanser tedavisi birincil tedavi olarak kullanılabilir, ancak en yaygın birincil kanser tedavisi cerrahidir. Kanser, radyasyon tedavisine veya kemoterapiye özellikle duyarlıysa, birincil tedavi olarak bu tedavilerden birini alabilirsiniz </p>
										</div>
									</div>
								</div>
								<div class="faq-item">
									<a data-toggle="collapse" data-parent="#faqAccordion1" href="#faqItem8" aria-expanded="false" class="collapsed"><span>2.</span>Da Vinci Robotik Cerrahi</a>
									<div id="faqItem8" class="collapse faq-item-content" role="tabpanel">
										<div>
											<p>Türkiye’de rahim, prostat, mide, pankreas, karaciğer, kolorekral ve baş-boyun kanserleri gibi hastalıklarda da vinci robotik cerrahi yöntemi başarılı bir şekilde uygulanmaktadır. Cerrahi hassasiyetin daha fazla olması, görselliğin gelişmişliği, ulaşılması zor alanlara daha kolay erişim gibi avantajlarının yanında erken taburculuk, kısa sürede iyileşme sürecinin varlığı nedeniyle birincil tedavi olarak operasyon önerilen hastalara Türkiye’de daha çok da vinci robotik cerrahi yöntemi uygulanır.</p>
										</div>
									</div>
								</div>
								<div class="faq-item">
									<a data-toggle="collapse" data-parent="#faqAccordion1" href="#faqItem9" aria-expanded="false" class="collapsed"><span>3.</span>Adjuvan Tedavi</a>
									<div id="faqItem9" class="collapse faq-item-content" role="tabpanel">
										<div>
											<p>Adjuvan tedavinin amacı, kanserin tekrarlama olasılığını azaltmak için birincil tedaviden sonra kalan kanser hücrelerini öldürmektir. Yaygın adjuvan tedaviler arasında kemoterapi, radyasyon tedavisi ve hormon tedavisi bulunur. Neoadjuvan tedavi benzerdir, ancak birincil tedaviyi daha kolay veya daha etkili hale getirmek için birincil tedaviden önce tedaviler kullanılır.</p>
										</div>
									</div>
								</div>
								<div class="faq-item">
									<a data-toggle="collapse" data-parent="#faqAccordion1" href="#faqItem10" aria-expanded="false" class="collapsed"><span>4.</span>Hafifletici Tedavi</a>
									<div id="faqItem10" class="collapse faq-item-content" role="tabpanel">
										<div>
											<p>Palyatif tedaviler, tedavinin yan etkilerini veya kanserin kendisinin neden olduğu belirti ve semptomları hafifletmeye yardımcı olabilir. Semptomları hafifletmek için cerrahi, radyasyon, kemoterapi ve hormon tedavisi kullanılabilir. Diğer ilaçlar ağrı ve nefes darlığı gibi semptomları hafifletebilir. Palyatif tedavi, kanserinizi iyileştirmeye yönelik diğer tedavilerle aynı anda kullanılabilir.</p>
										</div>
									</div>
								</div>
								<div class="faq-item">
									<a data-toggle="collapse" data-parent="#faqAccordion1" href="#faqItem11" aria-expanded="false" class="collapsed"><span>5.</span>Kemoterapi</a>
									<div id="faqItem11" class="collapse faq-item-content" role="tabpanel">
										<div>
											<p>Kemoterapi, kanser hücrelerini öldürmek için ilaçlar kullanır. </p>
										</div>
									</div>
								</div>
								<div class="faq-item">
									<a data-toggle="collapse" data-parent="#faqAccordion1" href="#faqItem12" aria-expanded="false" class="collapsed"><span>6.</span>Radyasyon Tedavisi (Radyoterapi)</a>
									<div id="faqItem12" class="collapse faq-item-content" role="tabpanel">
										<div>
											<p>Radyasyon tedavisi, kanser hücrelerini öldürmek için X-ışınları veya protonlar gibi yüksek güçlü enerji ışınları kullanır. Radyasyon tedavisi vücudunuzun dışındaki bir makineden gelebilir (dış ışın radyasyonu) veya vücudunuzun içine yerleştirilebilir (brakiterapi).
											</p>

										</div>
									</div>
								</div>
								<div class="faq-item">
									<a data-toggle="collapse" data-parent="#faqAccordion1" href="#faqItem13" aria-expanded="false" class="collapsed"><span>7.</span>Kemik İliği Nakli</a>
									<div id="faqItem13" class="collapse faq-item-content" role="tabpanel">
										<div>
											<p>Kemik iliğiniz, kemiklerinizin içindeki kan kök hücrelerinden kan hücrelerini oluşturan malzemedir. Kök hücre nakli olarak da bilinen kemik iliği nakli, kendi kemik iliği kök hücrelerini veya bir donörden alınanları kullanabilir. Kemik iliği nakli, doktorunuzun kanserinizi tedavi etmek için daha yüksek dozlarda kemoterapi kullanmasına izin verir. Hastalıklı kemik iliğini değiştirmek için de kullanılabilir.
											</p>

										</div>
									</div>
								</div>
								<div class="faq-item">
									<a data-toggle="collapse" data-parent="#faqAccordion1" href="#faqItem14" aria-expanded="false" class="collapsed"><span>8.</span>İmmünoterapi</a>
									<div id="faqItem14" class="collapse faq-item-content" role="tabpanel">
										<div>
											<p>Biyolojik terapi olarak da bilinen immünoterapi, kanserle savaşmak için vücudunuzun bağışıklık sistemini kullanır. Kanser, vücudunuzda kontrolsüz bir şekilde hayatta kalabilir çünkü bağışıklık sisteminiz onu bir davetsiz misafir olarak tanımaz. İmmünoterapi, bağışıklık sisteminizin kanseri görmesine ve ona saldırmasına yardımcı olabilir.
											</p>

										</div>
									</div>
								</div>
								<div class="faq-item">
									<a data-toggle="collapse" data-parent="#faqAccordion1" href="#faqItem15" aria-expanded="false" class="collapsed"><span>9.</span>Hormon Tedavisi</a>
									<div id="faqItem15" class="collapse faq-item-content" role="tabpanel">
										<div>
											<p>Bazı kanser türleri vücudunuzun hormonları tarafından beslenir. Örnekler arasında meme kanseri ve prostat kanseri sayılabilir. Bu hormonların vücuttan uzaklaştırılması veya etkilerinin engellenmesi kanser hücrelerinin büyümesinin durmasına neden olabilir.
											</p>

										</div>
									</div>
								</div>
								<div class="faq-item">
									<a data-toggle="collapse" data-parent="#faqAccordion1" href="#faqItem16" aria-expanded="false" class="collapsed"><span>10.</span>Hedefe Yönelik İlaç Tedavisi</a>
									<div id="faqItem16" class="collapse faq-item-content" role="tabpanel">
										<div>
											<p>Hedefe yönelik ilaç tedavisi, kanser hücrelerinin içinde hayatta kalmalarını sağlayan belirli anormalliklere odaklanır.
											</p>

										</div>
									</div>
								</div>
								<div class="faq-item">
									<a data-toggle="collapse" data-parent="#faqAccordion1" href="#faqItem17" aria-expanded="false" class="collapsed"><span>11.</span>Kriyoablasyon</a>
									<div id="faqItem17" class="collapse faq-item-content" role="tabpanel">
										<div>
											<p>Bu tedavi soğuk algınlığı ile kanser hücrelerini öldürür. Kriyoablasyon sırasında, cildinizden ve doğrudan kanserli tümöre ince, değnek benzeri bir iğne (kriyoprob) sokulur. Dokuyu dondurmak için kriyoprobe bir gaz pompalanır. Daha sonra dokunun çözülmesine izin verilir. Dondurma ve çözme işlemi kanser hücrelerini öldürmek için aynı tedavi seansında birkaç kez tekrarlanır.
											</p>

										</div>
