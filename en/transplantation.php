                        <h3>Things to Know About Organ Transplant Process</h3>
                        <p>The operations in which an organ is removed from a donor, that is, a giving body, and locating to another receiving body to take the place of harmed or missing organ are called as transplantation. <br><br>
                            The organs and grafts to be transplanted can be taken from living bodies or cadavers. <br><br>
                            Heart, kidneys, liver, lungs, pancreas, intestine, uterus, cornea and bone marrow are among the organs and grafts which can be successfully transplanted today. <br><br>
                            At this point we wish to make an important reminder for our foreign guests who plan to come to Turkey for organ and graft transplantation. <br><br>
                            According to laws, the organ transplantations which are executed by taking from cadavers can only be implemented on Turkish citizens. <br><br>
                            For this reason, our patients who are coming to Turkey for transplantation should necessarily come with their living donors. It is not possible to find donors from Turkey. <br><br>
                            The living donor should be at least a 4th degree relative of the patient. <br><br>
                            The 4th degree of consanguinity can be described like this: <br><br>
                            1st Degree Relatives: Mother, father, children. <br>
                            2nd Degree Relatives: Grandmother, grandfather, siblings, grandchildren. <br>
                            3rd Degree Relatives: Uncle, aunt, nieces and nephews. <br>
                            4th Degree Relatives: Cousins. <br><br>
                            If the receiving patient is married, at least the 4th degree relatives of the patient’s spouse can be also donors. <br><br>
                            Patients who can prove the consanguinity at least the 4th degree with the official records they have brought from their country are directly accepted. <br><br>
                            If patients who cannot find any donors from their relatives of at least the 4th degree find donor candidates from their distant relatives or acquaintances, they are presented to a committee which is called as ethical committee. If it is understood by the ethical committee that there is no monetary relationship between the patient and donor, permission for transplantation is approved. <br><br>
                            The most important factor in transplantations is the blood type compatibility between the receiver and donor. But the Rh factor is not important. <br><br>
                            • Receiver of the A or AB blood type can receive organs from donor of A blood type. <br>
                            • Receiver of B or AB blood type can receive organs from donor of B blood type. <br>
                            • Receiver of A, B, AB or O blood type can receive organs from donor of O (zero) blood type. <br>
                            • Receiver of AB blood type can receive organs from donor of AB blood type. <br> <br>
                            When the medical information of patient and donor is sent us, an evaluation is executed quickly and they are invited to Turkey if it is approved. The preparations of transplantation are completed by doing necessary examinations in our relevant hospitals. <br><br>
                            Needed durations of stay in Turkey can be stated roughly like this: <br><br>
                            - Kidney transplant: 1-1,5 months <br>
                            - Liver transplant: 1,5-2 months <br>
                            - Corneal transplant: 2-3 weeks <br>
                            - Bone marrow transplant: 12 months <br><br>
                            We are ready to fulfil all needs of patients, donors and companions during the treatment. <br><br>
                            Apart from the option of hotel, being able to produce comfortable and affordable solutions like renting flats for long stays, transfer and all other needs are organized by our teams. <br><br>
                            We would like to mention another operation which is important and will provide benefits for our patients as well. The “Cross Transplantations” which are executed in Turkey. Cross transplantation is a type of transplant in which the suitable donors of patients awaiting transplant give organs reciprocally to each other’s receivers. Cross transplantations can be executed by the mutual consent between patients who are recorded in any organ transplantation centre without applying ethical committee.
                        </p>

