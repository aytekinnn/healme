								How are cells collected in a bone marrow transplant? <br>
								There are two methods of collecting stem cells from a healthy donor. The first of these is to collect stem cells from the bone marrow. In this method, the donor is put to sleep under general anesthesia in the operating room and bone marrow is collected from the hip bone of the donor using special needles. The second method is the collection of stem cells, which are directed to the circulating blood from the bone marrow by applying growth factor to the donor, from the arm veins of the donor by means of an apheresis device. <br>
								How is a bone marrow transplant done? <br>

								Stem cells are given to the patient through a vein. For this purpose, intravascular devices called central and nosocatheters are used. <br>
								What are the stages of a bone marrow transplant? <br>

								We can divide the process in the transport unit into three stages. The first stage is the treatment process that we call the "preparation regimen". In this period, a suitable treatment is selected according to the disease and the age of the patient. The duration of treatment varies according to the protocol applied. In summary, the preparatory regime has three main objectives. First of all, it is aimed to destroy the diseased cells that cannot be detected in the patient, to make room for the donor cells by draining the bone marrow of the patient, and to prevent the patient's immune system from reacting to the donor cells by suppressing them. The second step is the administration of stem cells. The third stage is the proliferation of donor stem cells in the recipient's bone marrow. <br>

								After the patients are discharged from the transplant unit, they are followed up in the transplant polyclinic. Although the frequency of follow-up varies according to the situation, it is once a week or once a fortnight in the early period of the first 3 months. Until the first year is up, the patient should be checked regularly every month. In the advanced period, control periods are determined according to the patient's condition. <br>
								What are the problems that can be seen after bone marrow transplantation? <br>
								Graft versus host disease (GVHD) is one of the problems encountered in the early and late period after transplantation. This is a war initiated by the immune system cells of the donor against the recipient and causes damage to various tissues of the body. The most frequently damaged tissues are the skin, liver and gastrointestinal tract. It occurs with skin rash, itching, nausea, vomiting, diarrhea and deterioration in liver tests. This disease is seen in 30-40% of transplants from HLA-matched sibling donors, and 60-70% in transplants from unrelated donors. <br>
								The second important problem is that the donated stem cells cannot settle and multiply in the recipient bone marrow. It is evaluated by looking at the blood values, and in this case, re-transplantation is on the agenda. The reoccurrence of the disease after transplantation is another problem. After the transplant, the patient is open to infections and it is very important to protect themselves from infection. <br>
