						<div class="row">
							<div class="col-md-6">
								<ul>
									<li> <b> Physical Examination: </b> During the examination, your doctor may feel the bumps which can be signs of cancer. There can also be seen anomalies to indicate the presence of cancer like changes on skin colour or growth of an organ. </li>
									<li> <b> Laboratory Tests: </b> : Laboratory investigations like urinary and blood tests may be guiding for doctor to understand the reasons of cancer. For example, a common blood test named as complete blood cell count may reveal white blood cells in an extraordinary amount or type in leukaemia patients. </li>
