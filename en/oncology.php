									<li> <b> Biyopsi: </b> In biopsy, our doctor collects a cell sample for laboratory test. There are a couple of ways to collect samples. Suitability of a biopsy procedure depends on your cancer type and its location. In many situations the only way to diagnose cancer is biopsy.</li>
								</ul>
