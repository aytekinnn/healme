                            In colonoscopy, the pop-outs which look like finger and named as polyps inside of colon are investigated. If this abnormal structure can be taken, it is extracted from intestines with a small cut. When extraction is not possible, a biopsy sample is taken from the polyp and directed to pathology. Pathologists evaluate the cells in tissue with regard to canceration, this process is quite important for the early diagnosis and treatment of cancers.

