                        <p>Türkiye’de yapılan kemik iliği nakillerinde uzman ve tecrübeli hekimlerimiz hasta ile konuşarak olası risk ve komplikasyonları değerlendirir. Hastaya gereken bilgiler verildikten sonra işleme başlanır.</p>
                        <div class="mt-3 mt-lg-6"></div>
                        <h3>Graft- versus-host hastalığı (GVHD) nedir?</h3>
                        <div class="mt-0 mt-lg-4"></div>
                        <p>Dönorden alınan kemik iliği (kök hücre nakli) nakline allojenik nakil denir ve bu işlemde alıcının bağışıklık sistemine vericinin kök hücrelerinin saldırması sonucu Graft-versus-host hastalığı gelişir. Hastalık nakilden sonra herhangi bir zamanda ortaya çıkabilir. Eğer kök hücreler akraba olmayan vericiden alındıysa risk daha fazladır. Graft-versus-host hastalığı akut ve kronik olarak karşımıza çıkar. Nakilden sonra ilk aylarda gelişen akut GVHD, cilt yapısını, sindirim sistemini ve karaciğeri ilk olarak etkiler. Kronik GVHD ise daha uzun sürede ortaya çıkar ve multiorgan tutulumu ile seyreder. Belirtileri ise şu şekildedir;
                        </p>
