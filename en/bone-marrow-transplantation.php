                        <div class="mt-0 mt-lg-4"></div>
                        <p>Kemik iliği nakli olan hastalar minimal ya da hayatı ciddi anlamda tehdit eden ve hastaneye yatış gerektiren komplikasyonlarla karşı karşıya kalabilir. Bu riskler nakile ihtiyaç duyulan hastalık, nakil türü, hastanın yaşı ve genel sağlık durumu gibi faktörlerden etkilenir. Olası riskler ise aşağıdaki gibidir;</p>
