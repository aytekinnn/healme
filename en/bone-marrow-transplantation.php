                        <p>Otolog kök hücre nakli gerçekleşecekse hastanın kendi vücudundan kök hücreleri toplamak için aferez denilen yöntem kullanılır. Aferezden önce kök hücre üretimini artırmak ve hücrelerin daha kolay toplanabilmesi için günlük olarak çeşitli büyüme faktörleri vücuda enjekte edilir. Aferez işleminde damardan kan alındıktan sonra ve makinede dolaştırılır. Bu makine verilen kanı kök hücreler de dahil olmak üzere farklı bileşenlere ayırır. Elde edilen kök hücreler daha sonra kullanılmak üzere dondurulur ve kalan kan vücuda tekrar verilir. Allojenik kök hücre naklinde ise donör kullanılır. Vericinin kök hücreleri, kanından ya da kemik iliğinden elde edilir. Alıcı için hangisinin uygun olduğuna nakil ekibi karar vermektedir. Özel bir allojenik kök hücre nakli olan kordon kanı naklinde, anneler bebekleri dünyaya geldikten sonra göbek bağını bağışlar ve göbek bağından gelen kan dondurularak kemik iliği nakli için lazım olana kadar kordon kanı bankasında uygun koşullar altında saklanır.
                         <br>   Kemik iliği nakli intraveniz kateterden gerçekleşir ve işlem ağrısızdır. Hasta sıklıkla prosedür boyunca uyanıktır. Nakledilen kök hücreler zamanla kemik iliğinde hasarlanmış kök hücrelerin yerini alır. Donörden gelen bağışıklık faktörleri, daha sonra kanser hücreleri ile savaşır.
                        </p>
                        <h3>Kemik İliği Naklinden Sonra Vücutta Ne Olur?</h3>
                        <div class="mt-0 mt-lg-4"></div>
                        <p>Nakledilen kök hücreler alıcının vücuduna girdikten sonra kandan kemik iliğine girer. Kemik iliğinde zamanla çoğalır ve yeni, sağlıklı kök hücrelerinin oluşmasını sağlar. Bu işleme engraftasyon denir. Hastanın vücudundaki kan hücreleri sayısının normale dönmesi genellikle birkaç hafta sürmektedir. Uzman nakil ekibi hekimleri, hastanın nakil sonrası durumunu anlık olarak kan testleri ve diğer görüntüleme yöntemleri ile takip eder. Bulantı ve kusma gibi en sık görülen komplikasyonları önlemek için hastaya antiemetik (bulantı önleyici) ilaçlar başlanabilir. Enfeksiyon gibi risklere karşı hasta bakımı çok önemlidir. Olası bir risk, hastanın hastanede kalış süresini uzatacaktır. </p>
                        <p>Kemik iliği yeterince kan hücresi üretmeye başlayana kadar hasta trombosit ve eritrosit transfüzyonuna ihtiyaç olabilir. GVHD riskini önlemek için bağışıklık sistemi baskılayıcı (immünsüpresif) ilaçların kullanılması gerekebilir. Kemik iliği naklinden sonra hastanın iyileşme süreci uzun olabilir ve bu süreçte hekim, enfeksiyonlara karşı medikal tedavi tercih eder. Nakilden sonra diyet ve yaşam tarzı değişiklikleri çok önemlidir. Günlük tuz alımını sınırlamak, meyve, sebze, kepekli tahıllar, yağsız et, balık, zeytinyağı tüketmek gerekir. Alkol ve sigaradan kaçınılmalıdır. İmmünsüpresif olaç olan kalsinörin inhibitörlerinin etkisini azaltacağından greyfurt ve greyfurt suyu tüketilmemelidir. </p>
