                        <p>Kemik iliği nakli hasarlanmış ya da hastalıklı kemik iliğini değiştirmek amacıyla sağlıklı kan üreten kök hücrelerin vücuda aktarılması işlemidir. Kemik iliği nakline aynı zamanda kök hücre nakli de denir. Hastanın kemik iliğinden yeteri kadar sağlıklı kan hücreleri üretilemezse ya da organ çalışmasını durdurursa nakil gerçekleştirilir. Kişinin kendi vücudundan yapılan kemik iliği nakline otolog, bir donörden (verici) yapılan nakle ise allojenik nakil denir. Allojenik kemik iliği nakli bağışçının kanından, kalça kemiği içerisindeki kemik iliğinden ya da göbek kordonu kanından gerçekleştirilir. Bu işlemden önce hastalıklı hücreleri yok etmek ve vericinin hücrelerine hazırlanmak kişi, alıcı birey yüksek dozda kemoterapi veya rdayoterapi tedavisi alır. Akut lösemi, adrenolökodistrofi, kronik anemi, kemik iliği yetmezliği sendromları, kronik lösemi, hemoglobinopatiler, Hodgkin Lenfoma, immün yetmezlik durumları, konjenital metabolizma hastalıkları, Multiple Myeloma, Miyelodiplastik Sendromlar, nöroblastom, NonHodgkin Lenfoma, plazma hücre hastalıkları, POEMS (polinöropati, organomegali, endokrinopati, monoklonal gammopati ve deri değişiklikleri ile seyreden multisistem bir hastalık) Sendromu ve amiloidoz hastalıklarının tedavisinde tercih edilir. Otolog kök hücre nakli ise hasta bireyin kendi vücudunda sağlıklı kök hücrelerin alınmasıyla gerçekleştirilir. Bu işlem hastayı, dönorden alınacak kemik iliğinin doku reddi yaratabilmesi gibi olumsuz sonuçlardan kurtarır. Eğer vücut yeteri kadar sağlıklı kemik iliği hücresi üretebiliyorsa otolog kemik iliği nakli iyi bir seçenektir ve bu hücreler toplandıktan sonra dondurularak da saklanabilir. Hodgkin Lenfoma, NonHodgkin Lenfoma, Myelom, plazma hücre hastalıkları gibi durumlarda tercih edilir. </p>
